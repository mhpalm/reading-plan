    // Helper function to generate dates for the whole year
    function generateFullYearDates() {
        const dates = {};
        const year = new Date().getFullYear();
        
        for (let month = 1; month <= 12; month++) {
            const daysInMonth = new Date(year, month, 0).getDate();
            for (let day = 1; day <= daysInMonth; day++) {
                const date = `${month}/${day}`;
                // Default readings if not specified in mcheyneData
                dates[date] = mcheyneData[date] || [
                    "Reading 1",
                    "Reading 2",
                    "Reading 3",
                    "Reading 4"
                ];
            }
        }
        return dates;
    }

    const fullYearData = generateFullYearDates();
    const plan = [];
    let dayCount = 1;

